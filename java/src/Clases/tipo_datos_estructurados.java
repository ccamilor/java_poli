package Clases;

public class tipo_datos_estructurados {

        //TIPOS DE DATOS ESTRUCTURADOS

    //String: Se emplea usando una instancia de la clase String
    //Aunque parezca trabajar con datos primitivos
    String variableString = "Hola soy un caracter"; //Podemos usar m√©todos para jugar con los caracteres, mayusculas minusculas etc.


    //Vectores - Arrays: Se emplea para almacenar
    //un grupo de datos del mismo tipo

    //Forma 1
    int vectorNumeros1[] = new int[10];
    //Forma 2:
    int vectorNumeros2[] = new int[10];
    //Forma 2;
    int vectorNumeros3[] = new int[10];
    //Forma 4:
    int vectorNumeros4[];
    //Forma 5:
    int vectorNumeros5[] = {};
    //Forma 6:
    int vectorNumeros6[] = {9, 8, 7, 6, 5, 4, 3, 2, 1, 0};
    //Forma 7:
    int vectorNumeros7[] = new int[]{9, 8, 7, 6, 5, 4, 3, 2, 1, 0};


    //Matriz: Se emplea para almacenar un grupo de datos del mismo tipo
    //Tienen forma bidimensional basados en [x],[y]

    //Forma 1:
    int matrizNumeros1[][] = new int[4][5];
    //Forma 2:
    int matrizNumeros2[][] = new int[4][5];
    //Forma 3:
    int matrizNumeros3[][] = new int[4][5];
    //Forma 4:
    int matrizNumeros4[][];
    //Forma 5:
    int matrizNumeros5[][] = {};
    //Forma 6:
    int matrizNumeros6[][] = {{1,2},{3,9}};
    //Forma 7:
    int matrizNumeros7[][] = new int [][]{{6,2},{2,7}};
}
